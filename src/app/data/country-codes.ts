function swapObjectKeysAndValues(obj: Record<string, string>): Record<string, string> {
  const newObj: Record<string, string> = {};
  return Object.entries(obj).reduce((accum, val) => {
    const res = accum;
    const [key, value] = val;
    res[value] = key;
    return accum;
  }, newObj);
}

